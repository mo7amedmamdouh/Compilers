
public class Pair {
	
	public String name;
	public String regex;
	
	public Pair(){
		
	}
	public Pair(String name , String regex) {
		this.name = name;
		this.regex = regex;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRegex() {
		return regex;
	}
	public void setRegex(String regex) {
		this.regex = regex;
	}
	
	

}
